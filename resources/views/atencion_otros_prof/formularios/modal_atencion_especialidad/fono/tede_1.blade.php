                                        <div class="form-group fill" style="padding:10px">
                                            <p>• Antes de comenzar la aplicación del Test, el examinador debe estar suficientemente
                                            familiarizado con las consignas para darlas sin vacilación, con soltura y naturalidad. Debe
                                            conocer igualmente los criterios de calificación para apreciar rápidamente las respuestas del
                                            niño y facilitar así la ejecución del examen.<br></p>
                                            <p>
                                            • Para una buena aplicación de la prueba, es indispensable trabajar sólo con el niño, en una pieza
                                            tranquila y bien iluminada. Tratando de evitar en lo posible elementos perturbadores en el
                                            ambiente exterior.<br></p>
                                            <p>
                                            • El examinador debe recibir al sujeto en forma amable, una conversación de algunos instantes
                                            en un tono amistoso es indispensable para disipar todo sentimiento de ansiedad.<br></p>
                                            <p>
                                            • Para alcanzar una apreciación exacta de las posibilidades del sujeto hay que motivarlo y
                                            mantener su atención durante toda la prueba.<br></p>
                                            <p>
                                            • Es absolutamente necesario seguir las instrucciones del Manual, no modificar las consignas.<br></p>
                                            <p>
                                            • Si el niño no señala o dice que no sabe, deberá registrarse en el protocolo.<br></p>
                                            <p>
                                            • El margen de tiempo entre estímulo y respuesta es de un máximo de 5 segundos, al final de
                                            éstos se le dirá “sigamos adelante con éste” y se le señala con el dedo.<br></p>
                                            <p>
                                            • Si el niño pierde la línea hay que dejar constancia y se le indica la línea: “sigamos aquí”.<br></p>
                                            <p>
                                            • El examinador debe estar atento al movimiento de los ojos del niño, ya que muchos errores
                                            pueden deberse a pérdida de la línea y no a dificultades en el reconocimiento del estímulo.<br></p>
                                            <p>
                                            • En los ítems de “Errores Específicos”, el examinador deberá consignar la letra nombrada por el
                                            niño<br></p>
